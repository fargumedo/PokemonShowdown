package pokemonSearch.items;

public class VersionItem2 {
	
	private String name;
	private String url;
	
	public VersionItem2() {
		super();
	}
	
	public VersionItem2(String name, String url) {
		super();
		this.name = name;
		this.url = url;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
